function translate_spell_school(id) {
  if (id === "0") {
    return 'Abjuration';
  } else if (id === "1") {
    return 'Conjuration';
  } else if (id === "2") {
    return 'Divination';
  } else if (id === "3") {
    return 'Enchantment';
  } else if (id === "4") {
    return 'Evocation';
  } else if (id === "5") {
    return 'Illusion';
  } else if (id === "6") {
    return 'Necromancy';
  } else if (id === "7") {
    return 'Transmutation';
  } 
}
